  <script>
  /* eslint-disable global-require */
  import { ValidationProvider, ValidationObserver } from 'vee-validate'
  import VuexyLogo from '@core/layouts/components/Logo.vue'
  import {
    BRow, BCol, BLink, BButton, BForm, BFormCheckbox, BFormGroup, BFormInput, BInputGroup, BInputGroupAppend, BImg, BCardTitle, BCardText,
  } from 'bootstrap-vue'
  import { required, email } from '@validations'
  import { togglePasswordVisibility } from '@core/mixins/ui/forms'
  import store from '@/store/index'
  import useJwt from '@/auth/jwt/useJwt'
  import ToastificationContent from '@core/components/toastification/ToastificationContent.vue'

  export default {
    components: {
      VuexyLogo,
      BRow,
      BImg,
      BCol,
      BLink,
      BButton,
      BForm,
      BCardText,
      BCardTitle,
      BFormCheckbox,
      BFormGroup,
      BFormInput,
      BInputGroup,
      BInputGroupAppend,
      // validations
      ValidationProvider,
      ValidationObserver,
    },
    mixins: [togglePasswordVisibility],
    data() {
      return {
        status: '',
        firstname: '',
        lastname: '',
        userEmail: '',
        password: '',
        gdpr: false,
        companyName: '',
        country: '',
        ipAddress: '',
        isoAlpha2Country: '',
        companyAddress: '',
        sideImg: require('@/assets/images/pages/register-v2.svg'),
        // validation
        required,
        email,
      }
    },
    computed: {
      passwordToggleIcon() {
        return this.passwordFieldType === 'password' ? 'EyeIcon' : 'EyeOffIcon'
      },
      imgUrl() {
        if (store.state.appConfig.layout.skin === 'dark') {
          // eslint-disable-next-line vue/no-side-effects-in-computed-properties
          this.sideImg = require('@/assets/images/pages/register-v2-dark.svg')
          return this.sideImg
        }
        return this.sideImg
      },
    },
    methods: {
      register() {
        this.$refs.registerForm.validate().then(success => {
          if (success) {
            useJwt.clientToken()
              .then(res => {
                  let token = res.data.access_token
                  useJwt.register(token,{
                    firstname: this.firstname,
                    lastname: this.lastname,
                    email: this.userEmail,
                    password: this.password,
                    accountType: "COMPANY",
                    companyAddress: "test",
                    companyName: "test",
                    companyRegistrationNumber: "test",
                    country: "test",
                    gdpr: this.gdpr,
                    identifier: "test",
                    ipAddress: "test",
                    isoAlpha2Country: "test"
                  })
                    .then(response => {

                      // useJwt.setToken(response.config.headers.Authorization)
                      // useJwt.setRefreshToken(response.config.headers.Authorization)
                      // localStorage.setItem('userData', JSON.stringify(response.data))
                      // this.$ability.update(response.data)
                      this.$toast({
                          component: ToastificationContent,
                          props: {
                          title: `Client Token and Create User APIs hit successfully`,
                          icon: 'EditIcon',
                          variant: 'success',
                          },
                      })
                      return this.$router.push('/')
                    })
                    .catch(error => {
                         //   this.$refs.registerForm.setErrors(error)
                        this.$toast({
                            component: ToastificationContent,
                            props: {
                            title: `${error.response.data.errorMessage}`,
                            icon: 'EditIcon',
                            variant: 'error',
                            },
                        })
                    })

              })
              .catch(error => {
                // this.$refs.registerForm.setErrors(error)
                this.$toast({
                    component: ToastificationContent,
                    props: {
                    title: `${error.errorMessage}`,
                    icon: 'EditIcon',
                    variant: 'error',
                    },
                })
              })
          }
        })
      },
    },
  }
  /* eslint-disable global-require */
  </script>

